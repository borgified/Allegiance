#ifndef _Undo_H_
#define _Undo_H_

//////////////////////////////////////////////////////////////////////////////
//
//  Undo structure
//
//////////////////////////////////////////////////////////////////////////////

class IUndo : public IObject {
public:
    virtual TRef<IUndo> Apply() = 0;
};

//////////////////////////////////////////////////////////////////////////////
//
// MultiUndo a wrapper object for a multi step operation.
//
//////////////////////////////////////////////////////////////////////////////

class MultiUndo : public IUndo {
public:
    static TRef<MultiUndo> Create();

    virtual bool Add(IUndo* pundo) = 0;
};

#endif
